#include <stdlib.h>
#include "libft.h"

void	ft_strdel(char **as)
{
	if (!as || !*as)
		return ;
	free(*as);
	*as = 0;
}

char	*ft_strdell(char **as)
{
	ft_strdel(as);
	return (NULL);
}
